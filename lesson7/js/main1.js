function ajaxTest() {
    var test = new XMLHttpRequest()
    test.open("GET", "main.xml", true)
    test.onreadystatechange = function() {
        if (test.readyState == 4 && test.status == 200) {
            doc = test.responseXML;
            var txt = "<table style='border:0'><tr><th>Title</th><th>Description</th><th>Price</th><th>Release Date</th>"
            var a = doc.getElementsByTagName("title")
            var t = doc.getElementsByTagName("description")
            var r = doc.getElementsByTagName("release")
            var p = doc.getElementsByTagName("price")
            for (var i = 0; i < a.length; i++)
            {
                txt = txt + "<tr><td>";
                txt = txt + a[i].childNodes[0].nodeValue + "</td><td>"
                txt = txt + t[i].childNodes[0].nodeValue + "</td><td class='alignRight'>"
                txt = txt + p[i].childNodes[0].nodeValue + "</td>"
                txt = txt + r[i].childNodes[0].nodeValue + "</td></tr>"
            }
            txt = txt + "</table>"
            document.getElementById("content").innerHTML = txt
            console.log("ajax")
        }
    }
    test.send()
}

function jsonParse() {
    var xhr = new XMLHttpRequest()
    xhr.open("GET", "main.json", true)
    xhr.onload = function() {
        if (xhr.status == 200) {
            responseObject = JSON.parse(xhr.responseText)
            var newContent = ""
            for (var i = 0; i < responseObject.games.length; i++) {
                newContent += "<p>Entree: " + responseObject.games[i].Title + "<br>"
                newContent += "Price: " + responseObject.games[i].Description + "<br>"
                newContent += "Description: " + responseObject.games[i].Price + "<br>"
                newContent += "Release Date: " + responseObject.games[i].ReleaseDate.Day + ", " + responseObject.games[i].ReleaseDate.Month + ", " + responseObject.games[i].ReleaseDate.Year + "."
                newContent += "<hr>"
            }
            document.getElementById("content").innerHTML = newContent
            console.log("json")
        }
    }
    xhr.send()
}

